#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = (char *)ft_memalloc(size + 1)))
		return (NULL);
	while (size + 1 > 0)
		str[size--] = '\0';
	return (str);
}
