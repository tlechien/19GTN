#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		y;

	i = 0;
	y = 0;
	while (s1[i])
		i++;
	while (s2[y] && (size_t)y < n)
	{
		s1[i + y] = s2[y];
		y++;
	}
	s1[i + y] = '\0';
	return (s1);
}
